package org.jamocha.rete.rulecompiler.hokifisch;

public class RuleCompilingException extends Exception {

	public RuleCompilingException() {
	}

	public RuleCompilingException(String msg) {
		super(msg);
	}

	public RuleCompilingException(Throwable cause) {
		super(cause);
	}

	public RuleCompilingException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
